namespace cbb.core
{
    /// <summary>
    /// Specific window message type.
    /// </summary>
    public enum WindowType
    {
        /// <summary>
        /// The window to show information message.
        /// </summary>
        Information = 0,

        /// <summary>
        /// The window to show warning message.
        /// </summary>
        Warning = 1,

        /// <summary>
        /// The window to show error message.
        /// </summary>
        Error = 2,
    }
}
